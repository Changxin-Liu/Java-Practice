package pacman.ghost;

/**
 * A specified ghost called "Pinky" with the given colour and type
 */
public class Pinky extends Ghost{

    public Pinky(){
        super();
    }

    /**
     * Returns the colour of Pinky
     *
     * @return Pinky's colour
     */
    public String getColour(){
        return "#c397d8";
    }

    /**
     * Returns the type
     * @return Pinky's type
     */
    public GhostType getType(){
        return GhostType.PINKY;
    }
}
